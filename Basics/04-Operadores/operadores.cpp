/*  Los operadores nos permiten realizar operaciones
 *  en variables y sus valores.
 * 
 *  Hay distintos tipos:
 *      - Aritmeticos:  nos permiten realizar operaciones matematicas simples.
 *      - Asignacion:   nos permiten asinar un valor a una variable.
 *      - Comparacion:  realizan una comparacion entre dos valores y devuelven un booleano
 *      - Logicos:  nos permiten realizar operaciones logicas (or, and, not).
 */

#include <iostream>
using namespace std;

/*********** operadores aritmeticos: *************
 * 
 *  adicion:            10 + 10 = 20
 *  resta:              20 - 10 = 10
 *  multiplicacion:     10 * 10 = 100
 *  division:           100 / 10 = 10
 *  modulo:             1024 % 6 = 4
 *  incremento:         var++ = var + 1
 *  decremento:         var-- = var -1
 * 
 */

/*********** operadores de asignacion: ***********
 * 
 *  =           var = valor
 *  +=          var += valor == var + valor
 *  -=          var -= valor == var - valor
 *  *=          var *= valor == var * valor
 *  /=          var /= valor == var / valor
 *  %=          var %=  valor == var % valor
 */

/*********** operadores de comparacion ***********
 *  igual a:                ==
 *  desigual a:             !=
 *  mayor que:              >
 *  menor que:              <
 *  mayor o igual que:      >=
 *  menor o igual que:      <=
 */ 

/*********** operadores logicos ***********
 *  and:        && -> si ambos argumentos son true retorna true, si no false
 *  or:         || -> retorna true si algun elemento es true
 *  not:        !  -> retorna el negado del argumento
 */

